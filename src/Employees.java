    private void jPanel6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel6MouseClicked
        EmployeeData emp = new EmployeeData();
        emp.setVisible(true);
        emp.jLabel1.setText(" MODIFY EMPLOYEE");
        emp.jButton2.setText("UPDATE");
        
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        int selectedRowIndex = jTable1.getSelectedRow();
        emp.emp_id.setText(model.getValueAt(selectedRowIndex, 0).toString());
        emp.e_name.setText(model.getValueAt(selectedRowIndex, 1).toString());
        emp.e_username.setText(model.getValueAt(selectedRowIndex, 2).toString());
        emp.e_email.setText(model.getValueAt(selectedRowIndex, 3).toString());
        emp.e_pass.setText(model.getValueAt(selectedRowIndex, 4).toString());
        
        
        Object Ans1 = model.getValueAt(selectedRowIndex, 5);
        Object Ans2 = (String) model.getValueAt(selectedRowIndex, 6);
        
        if(Ans1 == null)
        {
            emp.S_Que.setSelectedIndex(2);
            emp.S_Ans.setText(model.getValueAt(selectedRowIndex, 6).toString());
        }
        else if(Ans2 == null)
        {
            emp.S_Que.setSelectedIndex(1);
            emp.S_Ans.setText(model.getValueAt(selectedRowIndex, 5).toString());
        }
        emp.accStatus.setSelectedItem((String) model.getValueAt(selectedRowIndex, 7));
    }//GEN-LAST:event_jPanel6MouseClicked

